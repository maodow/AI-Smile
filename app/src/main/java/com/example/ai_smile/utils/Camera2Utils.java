    /**
     * Lock the focus as the first step for a still image capture.
     */
    private void lockFocus() {
        try {
            Log.e("thread", " takePicture  " + Thread.currentThread().getId());
            // This is how to tell the camera to lock focus.
            if (autofocusSupported) {
                mPreviewRequestBuilder.set(CaptureRequest.CONTROL_AF_TRIGGER,
                        CameraMetadata.CONTROL_AF_TRIGGER_START);
            }
            // Tell #mCaptureCallback to wait for the lock.
            mState = STATE_WAITING_LOCK;
            previewSession.capture(mPreviewRequestBuilder.build(), mPreviewCaptureCallback,
                    backgroudHandler);
        } catch (CameraAccessException e) {
            e.printStackTrace();
        }
    }

    /**
     * Unlock the focus. This method should be called when still image capture sequence is
     * finished.
     */
    private void unlockFocus() {
        try {
            // Reset the auto-focus trigger
            if (autofocusSupported) {
                mPreviewRequestBuilder.set(CaptureRequest.CONTROL_AF_TRIGGER,
                        CameraMetadata.CONTROL_AF_TRIGGER_CANCEL);
            }
            setAutoFlash(mPreviewRequestBuilder);
            previewSession.capture(mPreviewRequestBuilder.build(), mPreviewCaptureCallback,
                    backgroudHandler);
            // After this, the camera will go back to the normal state of preview.
            mState = STATE_PREVIEW;
            previewSession.setRepeatingRequest(mPreviewRequest, mPreviewCaptureCallback,
                    backgroudHandler);
        } catch (CameraAccessException e) {
            e.printStackTrace();
        }
    }


    /**
     * start record video
     */
    public void startRecord() {
        if (supportCamera) {
            clearCurrentVideoPath();
            if (null == cameraDevice || !textureView.isAvailable() || null == mPreviewSize) {
                return;
            }
            try {
                closePreviewSession();
                setUpMediaRecorder();
                setUpPreviewReader();
                SurfaceTexture texture = textureView.getSurfaceTexture();
                assert texture != null;
                texture.setDefaultBufferSize(videoSize.getWidth(), videoSize.getHeight());
                final CaptureRequest.Builder recordBuilder = cameraDevice.createCaptureRequest(CameraDevice.TEMPLATE_RECORD);
                List<Surface> surfaces = new ArrayList<>();

                // Set up Surface for the camera preview
                Surface previewSurface = new Surface(texture);
                surfaces.add(previewSurface);
                recordBuilder.addTarget(previewSurface);


                surfaces.add(nv21Reader.getSurface());
                recordBuilder.addTarget(nv21Reader.getSurface());

                // Set up Surface for the MediaRecorder
                Surface recorderSurface = mediaRecorder.getSurface();
                recordBuilder.addTarget(recorderSurface);
                surfaces.add(recorderSurface);

                Rect zoomRect = mPreviewRequestBuilder.get(CaptureRequest.SCALER_CROP_REGION);


                if (zoomRect != null) {
                    recordBuilder.set(CaptureRequest.SCALER_CROP_REGION, zoomRect);
                }

                // Start a capture session
                // Once the session starts, we can update the UI and start recording
                cameraDevice.createCaptureSession(surfaces, new CameraCaptureSession.StateCallback() {

                    @Override
                    public void onConfigured(@NonNull CameraCaptureSession cameraCaptureSession) {
                        previewSession = cameraCaptureSession;
                        updatePreview(recordBuilder);
                        mediaRecorder.start();
                        isRecording = true;
                    }

                    @Override
                    public void onConfigureFailed(@NonNull CameraCaptureSession cameraCaptureSession) {
                        if (null != context) {
                            Toast.makeText(context, "Failed", Toast.LENGTH_SHORT).show();
                        }
                    }
                }, backgroudHandler);
            } catch (CameraAccessException e) {
                e.printStackTrace();
            }
        }
    }

